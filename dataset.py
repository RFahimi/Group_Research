life = life_raw.copy()

life["Country"] = life["Country"].apply(clean_country)

life = life[life["Country"].isin(countries)].copy()

year_cols = [c for c in life.columns if str(c).startswith("YR")]

life = life.melt(
    id_vars=["Country"],
    value_vars=year_cols,
    var_name="year",
    value_name="life_expectancy"
)

life = life.rename(columns={"Country": "country"})

life["year"] = life["year"].str.replace("YR", "", regex=False)
life["year"] = pd.to_numeric(life["year"], errors="coerce")

life["life_expectancy"] = pd.to_numeric(life["life_expectancy"], errors="coerce")

life = life[["country", "year", "life_expectancy"]].copy()

life.head()

len(life["country"].unique())

sorted(life["country"].unique())

"""Step 7 Preparing (Cleaning) "GNI" File"""

gni_raw.head()

gni_raw.columns

gni = gni_raw.copy()

gni["Country Name"] = gni["Country Name"].apply(clean_country)

gni = gni[gni["Country Name"].isin(countries)].copy()

year_cols = [c for c in gni.columns if "[YR" in str(c)]

gni = gni.melt(
    id_vars=["Country Name"],
    value_vars=year_cols,
    var_name="year",
    value_name="gni_ppp_current_international"
)

gni = gni.rename(columns={"Country Name": "country"})

gni["year"] = gni["year"].str.extract(r"(\d{4})")
gni["year"] = pd.to_numeric(gni["year"], errors="coerce")

gni["gni_ppp_current_international"] = pd.to_numeric(
    gni["gni_ppp_current_international"].replace("..", pd.NA),
    errors="coerce"
)

gni = gni[["country", "year", "gni_ppp_current_international"]].copy()

gni.head()

