"""Step 8 Preparing (Cleaning) "Health Expenditure" and "GDP" Files"""

gdphexp_raw["Series Name"].drop_duplicates().tolist()

gdp_part = gdphexp_raw[gdphexp_raw["Series Name"] == "GDP per capita (current US$)"].copy()

hexp_part = gdphexp_raw[gdphexp_raw["Series Name"] == "Current health expenditure per capita (current US$)"].copy()

"""Step 9 Reshaping GDP from wide format to long format"""

gdp_part["Country Name"] = gdp_part["Country Name"].apply(clean_country)
