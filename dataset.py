nurse_raw = pd.read_csv("Nursing and midwifery per 10000.csv")
life_raw = pd.read_csv("life_expectancy_all_countries.csv")
gdphexp_raw = pd.read_csv("GDP and health expenditure for  FCSL_data.csv")
gni_raw = pd.read_csv("GNI for FCSL_data.csv")

"""Step 3 Creating Countries List"""

countries = [countries_raw.columns[0]] + countries_raw.iloc[:, 0].dropna().tolist()
countries

"""3-1 Check Countries list"""

len(countries)

countries

"""3-2 Uniformity of country names"""

country_map = {
    "Congo, Dem. Rep.": "Congo, Democratic Republic of",
    "Democratic Republic of the Congo": "Congo, Democratic Republic of",
    "Congo, Rep.": "Congo, Republic of",
    "Republic of Congo": "Congo, Republic of",
    "Congo": "Congo, Republic of",
    "Venezuela": "Venezuela, RB",
    "Venezuela (Bolivarian Republic of)": "Venezuela, RB",
    "Yemen, Rep.": "Yemen, Republic of",
    "Yemen": "Yemen, Republic of",
    "West Bank and Gaza": "West Bank and Gaza (territory)",
    "Syrian Arab Rep.": "Syrian Arab Republic",
    "Timor Leste": "Timor-Leste",
    "Sao Tome and Principe": "São Tomé and Príncipe",
    "Micronesia, Fed. Sts.": "Micronesia, Federated States of",
    "Micronesia (Federated States of)": "Micronesia, Federated States of"
}

def clean_country(x):
    x = str(x).strip()
    return country_map.get(x, x)

"""Step 4 Preparing (Cleaning) "MD" file"""

md = md_raw[["Location", "Period", "FactValueNumeric"]].copy()
md = md.rename(columns={
    "Location": "country",
    "Period": "year",
    "FactValueNumeric": "physicians_per_10000"
})

md["country"] = md["country"].apply(clean_country)
md["year"] = pd.to_numeric(md["year"], errors="coerce")
md["physicians_per_10000"] = pd.to_numeric(md["physicians_per_10000"], errors="coerce")

md = md[md["country"].isin(countries)].copy()

md["physicians_per_1000"] = md["physicians_per_10000"] / 10

md = md[["country", "year", "physicians_per_1000"]].copy()
md.head()

"""4-1 Check "MD" file Counties"""

sorted(md["country"].unique())

len(md["country"].unique())

"""Step 5 Preparing (Cleaning) "Nursing" File"""

nurse = nurse_raw[["Location", "Period", "FactValueNumeric"]].copy()

nurse = nurse.rename(columns={
    "Location": "country",
    "Period": "year",
    "FactValueNumeric": "nurses_midwives_per_10000"
})

nurse["country"] = nurse["country"].apply(clean_country)
nurse["year"] = pd.to_numeric(nurse["year"], errors="coerce")
nurse["nurses_midwives_per_10000"] = pd.to_numeric(nurse["nurses_midwives_per_10000"], errors="coerce")

nurse = nurse[nurse["country"].isin(countries)].copy()

nurse["nurses_midwives_per_1000"] = nurse["nurses_midwives_per_10000"] / 10

nurse = nurse[["country", "year", "nurses_midwives_per_1000"]].copy()

nurse.head()

"""5-1 Check "Nursing" file Counties"""

sorted(nurse["country"].unique())

